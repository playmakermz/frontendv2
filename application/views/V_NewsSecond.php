            <ul>
                <li>
                    <a class="font-weight-bold">Jenis Pestisida</a>
                    <p>
                        Pestisida oleh para ahli dikelompokan untuk mempermudah pengenalanya. Pestisida dapat
                        dikelompokkan berdasarkan jenis sasaran, bentuk fisik, bentuk formulasi, cara kerjanya, cara masuk,
                        golongan senyawa, dan asal bahan aktifnya.
                    </p>
                    <p>
                        Ditinjau dari jenis organisme yang menjadi sasaran penggunaan pestisida dapat dibedakan menjadi
                        beberapa jenis antara lain:
                    </p>
                    <ol>
                        <li>Insektisida adalah bahan yang mengandung senyawa kimia yang bisa mematikan semua jenis serangga.</li>
                        <li>Fungisida adalah bahan yang mengandung senyawa kimia beracun dan bisa digunakan untuk memberantas dan mencegah fungsi/cendawan.</li>
                        <li>Disebut bakterisida karena senyawa ini mengandung bahan aktif beracun yang bisa membunuh bakteri.</li>
                        <li>Nermatisida, digunakan untuk mengendalikan nematoda.</li>
                        <li>Akarisida atau mitisida adalah bahan yang mengandung senyawa kimia yang digunakan untuk membunuh tungau, caplak dan laba-laba.</li>
                        <li>Rodenstisida adalah bahan yang mengandung senyawa kimia beracun yang digunakan untuk mematikan berbagai jenis binatang pengerat, misalnya tikus.</li>
                        <li>Moluskisida adalah pestisida untuk membunuh moluska, yaitu : siput,bekicot serta tripisan yang banyak dijumpai di tambak.</li>
                        <li>Herbisida adalah senyawa kimia beracun yang dimanfaatkan untuk membunuh tumbuhan pengganggu yang disebut gulma.</li>
                        <li>Ovisida, berasal dari kata latin ovum berarti telur, berfungsi untuk merusak telur. Pedukulisida, berasal dari kata latin pedis, berarti kutu, tuma, berfungsi untuk membunuh kutu atau tuma.</li>
                        <li>Piscisida, berasal dari kata Yunani Piscis, berarti ikan, berfungsi untuk membunuh ikan.</li>
                        <li>Termisida, berasal dari kata Yunani termes, artinya serangga pelubang kayu berfungsi untuk membunuh rayap.</li>
                    </ol>
                </li>
                <li>
                    <a class="font-weight-bold">Berdasarkan Sifat dan Cara Kerja Racun Pestisida</a>
                    <ol>
                        <li>Racun Kontak, Pestisida jenis ini bekerja dengan masuk ke dalam tubuh serangga sasaran lewat kulit (kutikula) dan di transportasikan ke bagian tubuh serangga tempat pestisida aktif bekerja.</li>
                        <li>Racun Pernafasan (Fumigan), Pestisida jenis ini dapat membunuh serangga dengan bekerja lewat sistem pernapasan.</li>
                        <li>Racun Lambung, Jenis pestisida yang membunuh serangga sasaran jika termakan serta masuk ke dalam organ pencernaannya</li>
                        <li>Racun Sistemik, Cara kerja seperti ini dapat memiliki oleh insektisida, fungisida dan herbisida. Racun sistemik setelah disemprotkan atau ditebarkan pada bagian tanaman akan terserap ke dalam jaringan tanaman melalui akar atau daun, sehingga dapat membunuh hama yang berada di dalam jaringan tanaman seperti jamur dan bakteri. Pada insektisida sistemik, serangga akan mati setelah memakan atau menghisap cairan tanaman yang telah disemprot.</li>
                        <li>Racun Metabolisme, Pestisida ini membunuh serangga dengan mengintervensi proses metabolismenya.</li>
                        <li>Racun Protoplasma, Ini akan mengganggu fungsi sel karena protoplasma sel menjadi rusak.</li>
                    </ol>
                </li>
                <li>
                    <a class="font-weight-bold">Berdasarkan Bentuk Fisiknya Pestisida dapat berupa</a>
                    <ol>
                        <li>Cair</li>
                        <li>Padat</li>
                        <li>Aerosol</li>
                    </ol>
                </li>
                <li>
                    <a class="font-weight-bold">Berdasarkan asal bahan aktif, pestisida dapat digolongkan menjadi :</a>
                    <ol>
                        <li>Sintetik Anorganik : garam-garam beracun seperti arsenat, flourida, tembaga sulfat dan garam merkuri</li>
                        <li>Organik Organo khlorin : DDT, SHC, endrin, dieldrin, dll.</li>
