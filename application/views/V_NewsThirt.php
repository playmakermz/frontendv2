            <h3 class="title">Metode Pemberian Pupuk Pada Tanaman</h3>
        </div>
        <div class="card-body">
            <div class="text-center mb-4">
                <img src="<?= base_url('assets/img/158147671727756_620.jpg') ?>" alt="" style="width: 70%; height:auto">
            </div>
            <p>
                Petani sangat membutuhkan pengetahuan tentang metode pemupukan yang benar. Ini berefek langsung pada tingkat efektivitas dan efisiensi penggunaan pupuk.
            </p>
            <p>
                Saat ini telah banyak pupuk yang beredar dengan berbagai bentuk. Mulai dari granular, tepung hingga cair. Semuanya tak bisa disamakan dalam aplikasinya,
                agar penyerapan unsur hara oleh tanaman bisa maksimal. Berikut beberapa metode pemupukan yang bisa dilakukan petani:
            </p>
            <ul>
                <li>
                    <a class="font-weight-bold">Disebar (broadcasting)</a>
                    <p>
                        Pemupukan dilakukan dengan cara meyebar pupuk secara merata pada tanah-tanah di sekitar pertanaman atau pada waktu pembajakan/penggaruan terakhir.
                    </p>
                    <p>
                        Pemupukan dengan cara disebar biasanya dilakukan sehari sebelum tanam, kemudian diinjak-injak agar pupuk masuk ke dalam tanah. Cara
                        pemupukan ini biasanya digunakan untuk memupuk tanaman padi, kacang-kacangan dan lain-lain yang mempunyai jarak tanam rapat.
                    </p>
                </li>
                <li>
                    <a class="font-weight-bold">Larikan atau Barisan (ring placement)</a>
                    <p>
                        Pemupukan dilakukan dengan cara menaburkan pupuk di antara larikan tanaman. Untuk tanaman tahunan, pupuk bisa ditaburkan melingkari
                        tanaman dengan jarak tegak lurus dengan daun terjauh (tajuk daun). Cara ini dilakukan dengan pertimbangan sebagai berikut:
                    </p>
                    <ol>
                        <li>pupuk yang digunakan relatif sedikit.</li>
                        <li>jarak tanam antartanaman yang dipupuk cukup jarang dan jarak antara barisan pertanaman cukup jarang.</li>
                        <li>kesuburan tanah rendah.</li>
                        <li>tanaman dengan perkembangan akar yang sedikit.</li>
                        <li>untuk tanah tegalan atau darat.</li>
                    </ol>
                </li>
                <li>
                    <a class="font-weight-bold">Ditempatkan dalam Lubang (spot placement)</a>
                    <p>
                        Pemupukan ini dilakukan dengan cara memebenamkan pupuk ke dalam lubang di samping batang sedalam kurang lebih 10 cm dan ditutup dengan tanah.
                        Atau bisa juga dikocor dengan cara menyiramkan pupuk jarak 5-10 cm dari pangkal batang.
                    </p>
                </li>
                <li>
                    <a class="font-weight-bold">Pemupukan Melalui Daun Tanaman (Spraying)</a>
                    <p>
                        Pemupukan ini dilakukan dengan cara melarutkan pupuk dalam air dengan konsentrasi sangat rendah kemudian disemprotkan langsung pada daun
                        dengan alat penyemprot biasa (hand sprayer). Sebelum melakukan penyemprotan, ada beberapa hal yang harus diperhatikan:
                    </p>
                    <ol>
                        <li>Konsentrasi larutan pupuk yang dibuat harus sangat rendah atau mengikuti petunjuk dalam kemasan pupuk. Jika konsentrasinya lebih rendah dari anjuran maka untuk mengimbanginya bisa memperbanyak frekuensi pemupukan, misalnya dari setiap 10 hari bisa dipercepat jadi seminggu sekali.</li>
                        <li>Pupuk daun disemprotkan ke bagian daun yang menghadap ke bawah karena mulut daun (stomata) umumnya menghadap ke bawah atau bagain punggung daun</li>
                        <li>Pupuk hendaknya disemprotkan ketika matahari tidak sedang terik-teriknya. Paling ideal dilakukan sore atau pagi</li>
                        <li>Penyemprotan pupuk daun tidak dilakukan pada saat hujan, karena beresiko pupuk daun akan habis tercuci oleh air hujan</li>
                    </ol>
                    <p>Pemberian pupuk daun bisa dilakukan bersamaan dengan pemberian pestisida jika dianggap perlu atau bersamaan dengan zat perangsang.</p>
                </li>
                <li>
                    <a class="font-weight-bold">Larangan dalam melakukan peyemprotan daun tanaman:</a>
                    <ul>
                        <li>Pada saat tunas telah muncul, penyemprotan dihentikan sebab tunas muda ini amat peka terhadap pupuk, apalagi kalau dosisnya melebihi dari yang dianjurkan.</li>
                        <li>Pada saat bunga mulai mekar, penyemprotan harus dihentikan agar bunga bakal buah yang dinanti-nanti tidak rontok. (keguguran).</li>
                        <li>Satu lagi tanaman yang tidak bisa disemprot pupuk daun ialah tanaman yang baru dipindah ke lapangan karena tanaman itu masih terhitung masih muda dan lemas.</li>
                    </ul>
                </li>
            </ul>
            <p>
                Aplikasi pemupukan pada tanaman semusim dan tahunan berbeda. Pada tanaman semusim seperti kacang-kacangan, sayuran, padi, jagung, dan lainnya menggunakan metode pemupukan secara disebar, dalam lubang, atau larikan. Sedangkan pada tanaman tahunan seperti tanaman buah-buahan, kopi, teh, kakao, kelapa, dan lainnya menggunakan metode ring placement.
            </p>

