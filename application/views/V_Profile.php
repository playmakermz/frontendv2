		<h3>Sejarah Desa</h3>
        <hr style="border: 0.1em solid black;">
		<p>
			Desa Kumpulrejo adalah desa kolonisasi yang datang dari beberapa daerah di Jawa Tengah dan Jawa Timur,
			Wilayah Timur ditandai dengan adaya tokoh sejarah dari jawa Tengah yaitu Ky Dami yang merupakan kaki tangan
			dari Temenggung Mertowijojo.
			Wilayah Timur merupakan kawasan pertanian, pada tahun 1857 penduduk desa dengan jumlah yang kurang dari 50
			kk.
			Mata pencaharian penduduk pada saat itu hanya mengandalkan hasil pertanian saja.
			mereka membuka dan menggarap tanah yang telah telah ada pada saat itu,
			sedangkan pada wilayah sebelah utara yang ditandai adanya Tokoh Sejarah Demang Aliman yang yang berasal dari
			jawa Timur Beliau sangat terkenal dengan kekuatan dan kadikdayaanya dan memiliki tombak yang dapat berubah
			menjadi ular besar
			jenis welang. Penduduk sebelah utara bermata pencaharian berkebun di sekeliling rumah dengan ditanamami
			tanaman polowijo diantaranya
			tanamam koro dan sebagian melaut untuk mencari ikan.
		</p>
		<p>
			Kata Kumpulrejo merupakan do’a yang berasal dari kata kumpul dan redjo.
			Kumpul mempunyai arti bergabung dalam kesatuan masyarakat dalam wilayah Dukuh
			( Marong wetan, Marong Kulon, Korowelang Lor dan Korowelang Kidul ) sedangkan kata Redjo mempunyai arti
			keberkahan hasil.
			Jadi Kumpulrejo berarti bergabungnya masyarakat dalam kesatuan wilayah Dukuh untuk kemakmuran dan
			Keberkahan.
		</p>
